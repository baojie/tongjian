#!/usr/bin/env python3
"""
batch_create_official.py — 批量创建官职页面

从原文 grep 引文（含中文卷号）并生成 stub 页面。

用法：
    python3 wiki/scripts/batch_create_official.py --type cishi --limit 10
    python3 wiki/scripts/batch_create_official.py --type taishou --limit 5
    python3 wiki/scripts/batch_create_official.py --dry-run --type cishi --limit 5
"""

import os, re, sys, subprocess, argparse

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.normpath(os.path.join(SCRIPT_DIR, "..", ".."))
PAGES_DIR = os.path.join(ROOT, "wiki", "public", "pages")
CORPUS = os.path.join(ROOT, "corpus", "raw", "资治通鉴.txt")

EXISTING = {f[:-3] for f in os.listdir(PAGES_DIR) if f.endswith('.md')}

# ── 中文数字解析 ──
CNUM = {'一':1,'二':2,'三':3,'四':4,'五':5,'六':6,'七':7,'八':8,'九':9,
        '十':10,'百':100,'千':1000}

def chinese_vol_to_int(s: str) -> int:
    """解析中文数字（已去除"第"前缀）为整数，如"第一十"→10,"一百一十"→110"""
    result = 0
    cur = 0
    for ch in s:
        if ch in CNUM and CNUM[ch] < 10:
            cur = CNUM[ch]
        elif ch in ('十','百','千'):
            scale = CNUM[ch]
            if cur == 0:
                cur = scale
            else:
                cur *= scale
            result += cur
            cur = 0
    result += cur
    return result

VOL_RE = re.compile(r'●卷第([一二三四五六七八九十百]+)')

def find_citations(title: str, max_n: int = 3):
    """从原文找引文，返回 [(vol_num, text_snippet), ...]"""
    if not os.path.exists(CORPUS):
        return []
    with open(CORPUS) as f:
        text = f.read()
    vol = 0
    results = []
    for line in text.split('\n'):
        vm = VOL_RE.search(line)
        if vm:
            vol = chinese_vol_to_int(vm.group(1))
            continue
        if title in line and vol > 0:
            clean = line.strip().replace('　', ' ')[:100]
            if clean:
                results.append((vol, clean))
                if len(results) >= max_n:
                    break
    return results


def guess_dynasty(title: str, vol: int) -> str:
    """从卷号推断朝代"""
    if vol <= 7: return "战国"
    if vol <= 15: return "秦"
    if vol <= 68: return "汉"
    if vol <= 78: return "三国"
    if vol <= 103: return "西晋"
    if vol <= 123: return "东晋"
    if vol <= 133: return "南北朝"
    if vol <= 169: return "南北朝"
    if vol <= 184: return "隋"
    if vol <= 220: return "唐"
    if vol <= 253: return "唐"
    return "五代十国"


def make_page(title: str, tt: str, citations: list, dry: bool = False) -> dict:
    if title in EXISTING:
        return {"s": title, "r": "exists"}

    # 从最早引文卷号推断朝代
    dynasty = ""
    if citations:
        dynasty = guess_dynasty(title, citations[0][0])

    # 标签、描述
    tag_map = {"cishi": "刺史", "taishou": "太守", "junjun": "将军", "jiedushi": "节度使"}
    desc_map = {
        "cishi": f"{title}，古代地方官职，掌一州军政民政，地位重要。",
        "taishou": f"{title}，古代郡守官职，掌一郡政务。",
        "junjun": f"{title}，古代将军号，魏晋南北朝时期常见的军号。",
        "jiedushi": f"{title}，唐代藩镇节度使。",
    }
    tag = tag_map.get(tt, "官职")
    desc = desc_map.get(tt, f"{title}，古代官职。")

    lines = [
        "---",
        f"id: {title}",
        "type: 官职",
        f"label: {title}",
    ]
    if dynasty:
        lines.append(f"dynasty: {dynasty}")
    lines.append(f"tags: [{tag}]")
    lines.append(f"description: {desc}")
    lines.append("---")
    lines.append("")
    lines.append(f"# [[{title}]]")
    lines.append("")
    lines.append("## 简介")
    lines.append("")
    lines.append(desc)
    lines.append("")
    lines.append("## 通鉴记载")
    lines.append("")

    for vol, txt in citations:
        lines.append(f"> {txt}")
        lines.append(f"> （{vol:03d}）")
        lines.append("")

    content = "\n".join(lines)

    # write temp file and call add_page.py
    tmp = f"/tmp/_bco_{title}.md"
    with open(tmp, "w") as f:
        f.write(content)

    if dry:
        os.remove(tmp)
        return {"s": title, "r": "dry"}

    add_script = os.path.join(SCRIPT_DIR, "add_page.py")
    result = subprocess.run(
        ["python3", add_script, title, tmp],
        capture_output=True, text=True, cwd=ROOT
    )
    os.remove(tmp)

    if result.returncode == 0:
        return {"s": title, "r": "ok"}
    return {"s": title, "r": "err", "e": result.stderr.strip()}



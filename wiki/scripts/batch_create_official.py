def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--type", choices=["cishi", "taishou", "junjun", "jiedushi", "generic"], default="cishi")
    ap.add_argument("--limit", type=int, default=10)
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()

    titles = {"cishi": CISHI, "taishou": TAISHOU, "junjun": JUNJUN, "jiedushi": JIEDUSHI, "generic": GENERIC}[args.type]
    labels = {"cishi": "州刺史", "taishou": "太守", "junjun": "军号将军", "jiedushi": "节度使", "generic": "官职"}

    todo = [t for t in titles if t not in EXISTING][:args.limit]
    print(f"准备创建 {len(todo)}/{len(titles)} 个{labels[args.type]}页面{'（预览）' if args.dry_run else ''}")

    for title in todo:
        cits = find_citations(title)
        r = make_page(title, args.type, cits, args.dry_run)
        status = {"ok": "✓", "dry": "·", "exists": "—", "err": "✗"}.get(r["r"], "?")
        extra = ""
        if r["r"] == "ok":
            extra = f" ({len(cits)}条引文)"
        elif r["r"] == "dry":
            extra = f" ({len(cits)}条引文)"
        elif r["r"] == "err":
            extra = f" {r.get('e','')}"
        print(f"  {status} {title:16s}{extra}")

    print("完成。")


if __name__ == "__main__":
    main()

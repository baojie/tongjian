def is_valid_name(name: str) -> bool:
    # 人名最多4字（5字几乎总是「职衔尾段+人名」的误捕）
    if not name or len(name) < 2 or len(name) > 4:
        return False
    if name in NOISE:
        return False
    # 含标点（含顿号）
    if re.search(r'[，。；：\s0-9、·]', name):
        return False
    if name[0] in _BAD_START:
        return False
    if name[-1] in _BAD_END:
        return False
    # 名字内部含动词/介词（如「欢还鄴」「泰军广阳」「亮欲」）
    if any(c in _BAD_ANYWHERE for c in name):
        return False
    # 3字以上名字若中间含「王」或「侯」，几乎必为「[封地]王/侯[名]」爵号
    if len(name) >= 3 and ('王' in name[1:-1] or '侯' in name[1:-1]):
        return False
    # 名字中不应包含官职词
    for term in _POSITION_TERMS:
        if term in name:
            return False
    # 4字名以郡望/封国开头，几乎必为「郡望+人名」误提取
    if len(name) == 4:
        for prefix in _COUNTY_PREFIXES:
            if name.startswith(prefix):
                return False
    return True


@dataclass
class Appointment:
    person: str
    vol: int
    para: int
    context: str


def _try_strip_prefix(name: str) -> str:
    """尝试剥离名字前的官职词/郡望/地名前缀，返回剥离后的名字（可能仍无效）。"""
    # 官职/爵号前缀（太尉灌婴→灌婴，客卿烛寿→烛寿）
    for term in _POSITION_TERMS:
        if len(term) >= 2 and name.startswith(term):
            return name[len(term):]
    # 郡望/封国前缀（颍川铫期→铫期，琅邪王睿→王睿）
    for prefix in _COUNTY_PREFIXES:
        if name.startswith(prefix):
            return name[len(prefix):]
    # 「赵人」「齐人」「吴人」等地理籍贯前缀（赵人楼缓→楼缓）
    if len(name) >= 4 and name[1] == '人':
        candidate = name[2:]
        if len(candidate) >= 2:
            return candidate
    return name


def extract_holders(position: str, pages_dir: Path) -> list[Appointment]:
    patterns = make_appt_patterns(position)
    seen: dict[tuple[str, int], Appointment] = {}

    for md in sorted(pages_dir.glob("第*.md")):
        text = md.read_text(encoding="utf-8")
        for m in PARA_RE.finditer(text):
            vol, para = int(m.group(1)), int(m.group(2))
            seg = m.group(3)
            clean = clean_wikilinks(seg)
            if position not in clean:
                continue
            for pat in patterns:
                for pm in pat.finditer(clean):
                    name = pm.group(1).strip()
                    # 先尝试剥离官职/郡望/地名前缀
                    stripped = _try_strip_prefix(name)
                    if stripped != name and is_valid_name(stripped):
                        name = stripped
                    elif not is_valid_name(name):
                        continue
                    key = (name, vol)
                    if key not in seen:
                        # 以匹配位置为中心，截取前后语境窗口，锚定到句边界
                        match_center = (pm.start() + pm.end()) // 2
                        ctx_start = max(0, match_center - 30)
                        ctx_end = min(len(clean), match_center + 50)
                        # 向前锚定到句尾（。；！？\n）
                        for sep in ('。', '；', '！', '？', '\n'):
                            idx = clean.rfind(sep, ctx_start - 30, match_center)
                            if idx != -1:
                                ctx_start = idx + 1
                                break
                        # 向后锚定到句尾
                        for sep in ('。', '；', '！', '？', '\n'):
                            idx = clean.find(sep, match_center, ctx_end + 30)
                            if idx != -1:
                                ctx_end = idx + 1
                                break
                        ctx = clean[ctx_start:ctx_end].strip()
                        ctx = ' '.join(ctx.split())
                        seen[key] = Appointment(
                            person=name,
                            vol=vol,
                            para=para,
                            context=ctx,
                        )

    # 按人名去重：若「史思明」和「思明」都出现，保留更长的那个
    by_name: dict[str, Appointment] = {}
    for appt in seen.values():
        name = appt.person
        # 检查是否是已有记录的子串（「思明」⊂「史思明」）
        dominated = False
        for existing_name in list(by_name.keys()):
            if name in existing_name:
                dominated = True
                break
            if existing_name in name:
                # 新记录更长，替换
                del by_name[existing_name]
        if not dominated:
            if name not in by_name or appt.vol < by_name[name].vol:
                by_name[name] = appt

    result = sorted(by_name.values(), key=lambda a: (a.vol, a.para))
    return result



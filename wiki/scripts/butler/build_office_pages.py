def format_pn(vol: int, para: int) -> str:
    return f"（{vol:03d}-{para:03d}）"


def build_full_page(spec: OfficeSpec, holders: list[Appointment]) -> str:
    tags_yaml = ', '.join(f'"{t}"' for t in spec.tags)
    aliases_yaml = f'["{spec.parent}", "{spec.label}"]'
    fm = f"""---
id: {spec.name}
type: 官职
label: {spec.label}
aliases: {aliases_yaml}
dynasty: {spec.dynasty}
tags: [{tags_yaml}]
description: {spec.description}
---"""

    table_rows = []
    for a in holders:
        pn = format_pn(a.vol, a.para)
        ctx = a.context[:40].replace('|', '｜')
        name_link = f"[[{a.person}]]"
        table_rows.append(f"| {name_link} | {pn} | {ctx} |")

    extras: list[str] = []
    if '节度使' in spec.name:
        extras = ['节度使', '藩镇']
    elif '都护' in spec.name:
        extras = ['都护']
    elif '观察使' in spec.name:
        extras = ['观察使']
    elif '刺史' in spec.name:
        extras = ['刺史']
    elif '太守' in spec.name:
        extras = ['太守']
    elif '仆射' in spec.name:
        extras = ['仆射', '尚书省']
    elif '都督' in spec.name:
        extras = ['都督']
    elif spec.name == '枢密使':
        extras = ['枢密院']
    see_also = set([spec.parent] + extras)
    see_lines = '\n'.join(f'- [[{s}]]' for s in sorted(see_also) if s != spec.name)
    body = f"""# [[{spec.name}]]

## 简介

{spec.description}

## 历任列表

| 任职者 | 首见引注 | 语境摘要 |
|--------|----------|----------|
""" + '\n'.join(table_rows) + f"""

## 参见

{see_lines}
"""
    return fm + '\n' + body


def build_page_content(holders: list[Appointment]) -> str:
    """仅返回历任表格节（无 frontmatter、无参见），用于更新已有页。"""
    table_rows = []
    for a in holders:
        pn = format_pn(a.vol, a.para)
        ctx = a.context[:40].replace('|', '｜')
        name_link = f"[[{a.person}]]"
        table_rows.append(f"| {name_link} | {pn} | {ctx} |")

    lines = [
        f"## 历任列表",
        f"",
        f"| 任职者 | 首见引注 | 语境摘要 |",
        f"|--------|----------|----------|",
    ] + table_rows
    return '\n'.join(lines)



def create_or_update_page(spec: OfficeSpec, holders: list[Appointment],
                          pages: dict, dry_run: bool) -> str:
    slug = spec.name
    exists = page_exists(slug, pages)

    if dry_run:
        action = "update" if exists else "create"
        return f"[dry] {action} {slug}"

    if not exists:
        full_page = build_full_page(spec, holders)
        with tempfile.NamedTemporaryFile(mode='w', suffix='.md',
                                        encoding='utf-8', delete=False) as f:
            f.write(full_page)
            tmp = f.name
        try:
            r = subprocess.run(
                [sys.executable, str(ADD_PAGE), slug, tmp,
                 '--summary', f'新增词条：{slug}（历任官员表格）',
                 '--author', 'butler'],
                capture_output=True, text=True, cwd=ROOT,
            )
            if r.returncode != 0:
                return f"✗ create {slug}: {r.stderr.strip()[:100]}"
            return f"✓ created {slug}"
        finally:
            Path(tmp).unlink(missing_ok=True)
    else:
        md_path = PAGES_DIR / f"{slug}.md"
        existing = md_path.read_text(encoding='utf-8')
        new_table_section = build_page_content(holders)
        if '## 历任列表' in existing:
            # 在「## 历任列表」处拆分，保留之前的内容（frontmatter + 前面的节）
            before_section, rest = existing.split('## 历任列表', 1)
            print(f"  [debug] before_section={len(before_section)}b rest={len(rest)}b", file=sys.stderr)
            print(f"  [debug] rest[:50]={rest[:50]!r}", file=sys.stderr)
            print(f"  [debug] rest[-50:]={rest[-50:]!r}", file=sys.stderr)

            # 逐行扫描 rest，找到第一个真正的 ## 节（其后内容不是 | 开头的表格行）
            # 跳过伪section（如 ## 初元 等artifact，其后紧跟 | 表格行）
            rest_lines = rest.split('\n')
            real_section_start = None
            for i, line in enumerate(rest_lines):
                if line.startswith('## '):
                    # 检查此节后第一个非空行是否以 | 开头
                    has_table = False
                    for j in range(i + 1, min(i + 5, len(rest_lines))):
                        stripped = rest_lines[j].strip()
                        if not stripped:
                            continue
                        if stripped.startswith('|'):
                            has_table = True
                        break
                    if not has_table:
                        real_section_start = i
                        print(f"  [debug] real section found at line {i}: {line!r}", file=sys.stderr)
                        break
                    else:
                        print(f"  [debug] skip pseudo-section at line {i}: {line!r}", file=sys.stderr)

            if real_section_start is not None:
                # 保留从合法节开始的所有内容
                after = '\n'.join(rest_lines[real_section_start:])
                print(f"  [debug] after={len(after)}b", file=sys.stderr)
                new_text = before_section + new_table_section + '\n' + after
            else:
                # 没有后继合法节，整个 rest 是旧表格数据，丢弃
                print(f"  [debug] no real section found, dropping rest ({len(rest_lines)} lines)", file=sys.stderr)
                new_text = before_section + new_table_section
        else:
            new_text = existing.rstrip() + '\n\n' + new_table_section + '\n'
        if new_text == existing:
            return f"~ unchanged {slug}"
        md_path.write_text(new_text, encoding='utf-8')
        subprocess.run(
            [sys.executable, str(RECORD), slug,
             '--summary', '更新历任列表',
             '--author', 'butler'],
            capture_output=True, cwd=ROOT,
        )
        return f"✓ updated {slug}"


def main():
    dry_run = '--dry-run' in sys.argv
    filter_pos = None
    for i, arg in enumerate(sys.argv):
        if arg == '--position' and i + 1 < len(sys.argv):
            filter_pos = sys.argv[i + 1]

    pages = load_registry()

    targets = [s for s in ALL_OFFICES
               if filter_pos is None or s.name == filter_pos]

    print(f"处理 {len(targets)} 个职位{'（DRY RUN）' if dry_run else ''}...")

    for spec in targets:
        holders = extract_holders(spec.name, PAGES_DIR)
        if not holders:
            print(f"  {spec.name}: 未找到任职记录，跳过")
            continue

        result = create_or_update_page(spec, holders, pages, dry_run)
        print(f"  {result} [{len(holders)} 条任职记录]")

    print("完成。")


if __name__ == '__main__':
    main()
